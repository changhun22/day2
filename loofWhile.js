let i = 0;
while (i < 10) {
    alert(i);
    i++;
}


// while문은 while (조건) { 내용 } 이렇게 구성된다 for문과는 같은 결과를 내지만,
// 시작부분은 while 전에 써주고, 끝 부분은 while 내용 안에 직접 써주어야 한다.

// while문과 for문은 언제 사용하는가?
// while은 조건밖에 없기 때문에 몇 번 반복될 지 모를 때 주로 사용되고,
// for문은 반복 횟수를 정할 수 있기 때문에 몇 번 반복될 지 알 때 주로 사용된다.