for (let i = 0; i < 10; i++) {

    console.log(i)
}

// for문은 for (시작; 조건; 끝) { 내용 } 이렇게 구성 되어 있다
// 위의 문장을 보면 시작은 let i = 0;으로 하고, 조건은 i < 10이고, 끝은 i++ 이다
// 즉, for문을 돌기 전에 let i = 0;으로 시작 i라는 변수를 만들고 0 값을 넣어 초기화
// 그러고나서 조건을 비교 i가 0이니까 10보다 작으므로 내용을 실행합니다. 그 후 i++를 실행 그래서 i가 1이 된다
// 이제부터 계속 반복한다. 조건을 비교하고, 조건과 맞으면 내용을 실행한 후  i++를 한다
// i가 10이 될 때까지 반복을 하며 0부터 9까지 10번 출력된다.